/* ================================================================
 * Render Single Character
 *
 * Draws char 'c' at pixel position (x, y) on surface.
 * fg = foreground color, bg = background color.
 * If bg has alpha=0 (0x00xxxxxx), background is transparent.
 * ================================================================ */

static void font_draw_char(fb_surface_t *s, uint32_t x, uint32_t y,
                           char c, uint32_t fg, uint32_t bg)
{
    int row, col;
    uint8_t bits;
    int ch_idx;
    uint32_t *row_ptr;
    int transparent_bg;

    ch_idx = (int)(unsigned char)c;
    if (ch_idx >= FONT_CHARS) ch_idx = 0;

    transparent_bg = ((bg >> 24) == 0) ? 1 : 0;

    row = 0;
    while (row < FONT_HEIGHT) {
        if (y + row >= s->height) break;
        bits = font_8x16[ch_idx][row];
        row_ptr = (uint32_t *)((uint8_t *)s->pixels + (y + row) * s->pitch);

        col = 0;
        while (col < FONT_WIDTH) {
            if (x + col < s->width) {
                if (bits & (0x80 >> col)) {
                    row_ptr[x + col] = fg;
                } else if (!transparent_bg) {
                    row_ptr[x + col] = bg;
                }
            }
            col = col + 1;
        }
        row = row + 1;
    }
}

/* ================================================================
 * Render String
 *
 * Draws null-terminated string at (x, y). No wrapping.
 * Returns x position after last character (for cursor placement).
 * ================================================================ */

static uint32_t font_draw_string(fb_surface_t *s, uint32_t x, uint32_t y,
                                 const char *str, uint32_t fg, uint32_t bg)
{
    uint32_t cx;
    int i;

    cx = x;
    i = 0;
    while (str[i] != 0) {
        if (str[i] == '\n') {
            y = y + FONT_HEIGHT;
            cx = x;
        } else {
            font_draw_char(s, cx, y, str[i], fg, bg);
            cx = cx + FONT_WIDTH;
        }
        i = i + 1;
        if (cx >= s->width) break;
    }
    return cx;
}

/* ================================================================
 * String Length (inline — no libc)
 * ================================================================ */

static uint32_t font_strlen(const char *s)
{
    uint32_t len;
    len = 0;
    while (s[len] != 0) len = len + 1;
    return len;
}

/* ================================================================
 * Render Centered String
 * ================================================================ */

static void font_draw_centered(fb_surface_t *s, uint32_t y,
                                uint32_t area_x, uint32_t area_w,
                                const char *str, uint32_t fg, uint32_t bg)
{
    uint32_t text_w;
    uint32_t x;

    text_w = font_strlen(str) * FONT_WIDTH;
    if (text_w >= area_w) {
        x = area_x;
    } else {
        x = area_x + (area_w - text_w) / 2;
    }
    font_draw_string(s, x, y, str, fg, bg);
}

/* ================================================================
 * Integer to Decimal String (for displaying numbers)
 * ================================================================ */

static void font_int_to_str(int val, char *buf, int buf_size)
{
    int i, j, neg, tmp;
    char swap;

    if (buf_size < 2) { buf[0] = 0; return; }

    neg = 0;
    if (val < 0) { neg = 1; val = -val; }

    i = 0;
    if (val == 0) {
        buf[i] = '0'; i = i + 1;
    } else {
        while (val > 0 && i < buf_size - 2) {
            buf[i] = '0' + (val % 10);
            val = val / 10;
            i = i + 1;
        }
    }
    if (neg && i < buf_size - 1) { buf[i] = '-'; i = i + 1; }

    /* Reverse */
    j = 0;
    tmp = i - 1;
    while (j < tmp) {
        swap = buf[j]; buf[j] = buf[tmp]; buf[tmp] = swap;
        j = j + 1; tmp = tmp - 1;
    }
    buf[i] = 0;
}

/* ================================================================
 * Hex to String (for addresses/values)
 * ================================================================ */

static void font_hex_to_str(uint32_t val, char *buf, int digits)
{
    int i;
    uint32_t nibble;
    buf[0] = '0'; buf[1] = 'x';
    i = digits - 1;
    while (i >= 0) {
        nibble = val & 0xF;
        if (nibble < 10) buf[2 + i] = '0' + nibble;
        else             buf[2 + i] = 'A' + nibble - 10;
        val = val >> 4;
        i = i - 1;
    }
    buf[2 + digits] = 0;
}
