use bootloader_api::info::{FrameBufferInfo, PixelFormat};

/// Framebuffer global
pub struct Framebuffer {
    buffer: &'static mut [u8],
    info: FrameBufferInfo,
}

impl Framebuffer {
    /// Crear nuevo framebuffer
    pub fn new(buffer: &'static mut [u8], info: FrameBufferInfo) -> Self {
        Framebuffer { buffer, info }
    }

    /// Obtener ancho
    pub fn width(&self) -> usize {
        self.info.width
    }

    /// Obtener alto
    pub fn height(&self) -> usize {
        self.info.height
    }

    /// Dibujar pixel
    pub fn draw_pixel(&mut self, x: usize, y: usize, color: Color) {
        if x >= self.info.width || y >= self.info.height {
            return;
        }

        let pixel_offset = y * self.info.stride + x;
        let bytes_per_pixel = self.info.bytes_per_pixel;
        let byte_offset = pixel_offset * bytes_per_pixel;

        let color_bytes = match self.info.pixel_format {
            PixelFormat::Rgb => [color.r, color.g, color.b, 0],
            PixelFormat::Bgr => [color.b, color.g, color.r, 0],
            PixelFormat::U8 => [color.r, 0, 0, 0],
            _ => [color.r, color.g, color.b, 0],
        };

        for (i, &byte) in color_bytes.iter().take(bytes_per_pixel).enumerate() {
            if byte_offset + i < self.buffer.len() {
                self.buffer[byte_offset + i] = byte;
            }
        }
    }

    /// Dibujar rectÃ¡ngulo
    pub fn draw_rect(&mut self, x: usize, y: usize, w: usize, h: usize, color: Color) {
        for dy in 0..h {
            for dx in 0..w {
                self.draw_pixel(x + dx, y + dy, color);
            }
        }
    }

    /// Limpiar pantalla
    pub fn clear(&mut self, color: Color) {
        for y in 0..self.info.height {
            for x in 0..self.info.width {
                self.draw_pixel(x, y, color);
            }
        }
    }

    /// Dibujar bandera de PerÃº
    pub fn draw_peru_flag(&mut self, x: usize, y: usize, width: usize, height: usize) {
        let stripe_height = height / 3;
        
        // Franja roja superior
        self.draw_rect(x, y, width, stripe_height, Color::RED);
        
        // Franja blanca central
        self.draw_rect(x, y + stripe_height, width, stripe_height, Color::WHITE);
        
        // Franja roja inferior
        self.draw_rect(x, y + stripe_height * 2, width, stripe_height, Color::RED);
    }

    /// Dibujar texto simple (8x8 font)
    pub fn draw_char(&mut self, x: usize, y: usize, c: char, color: Color) {
        let font = get_char_bitmap(c);
        for (row, &bits) in font.iter().enumerate() {
            for col in 0..8 {
                if bits & (0x80 >> col) != 0 {
                    self.draw_pixel(x + col, y + row, color);
                }
            }
        }
    }

    /// Dibujar string
    pub fn draw_string(&mut self, x: usize, y: usize, s: &str, color: Color) {
        let mut cx = x;
        for c in s.chars() {
            if c == '\n' {
                continue;
            }
            self.draw_char(cx, y, c, color);
            cx += 9; // 8 pixels + 1 espacio
        }
    }
}

/// Color RGB
#[derive(Clone, Copy)]
pub struct Color {
    pub r: u8,
    pub g: u8,
    pub b: u8,
}

impl Color {
    pub const BLACK: Color = Color { r: 0, g: 0, b: 0 };
    pub const WHITE: Color = Color { r: 255, g: 255, b: 255 };
    pub const RED: Color = Color { r: 205, g: 33, b: 42 }; // Rojo peruano
    pub const GREEN: Color = Color { r: 0, g: 255, b: 0 };
    pub const BLUE: Color = Color { r: 0, g: 0, b: 255 };
    pub const YELLOW: Color = Color { r: 255, g: 255, b: 0 };
    pub const CYAN: Color = Color { r: 0, g: 255, b: 255 };
    
    pub const fn new(r: u8, g: u8, b: u8) -> Self {
        Color { r, g, b }
    }
}

