pub fn is_d3d12_symbol(name: &str) -> bool {
    D3D12_FUNCTIONS.contains(&name)
        || D3D12_INTERFACES.contains(&name)
        || D3D12_TYPES.contains(&name)
        || D3D12_CONSTANTS.iter().any(|(n, _)| *n == name)
        || D3D12_IIDS.iter().any(|(n, _)| *n == name)
}
